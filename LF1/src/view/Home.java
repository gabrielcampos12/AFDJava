    private void getStateStart(String s){
        for(State state : states){
            if(state.getName().equals(s)){
                state.setStart(true);
            }
        }
        
    }
    private void transitionState(String pState,String nState, String symbol){
        for(State s : states){
            if(s.getName().equals(pState)){
                for(State s2 : states){
                    if(s2.getName().equals(nState)){
                        s.getTransition().put(symbol, s2);
                    }
                }
            }
        }
    }

  
