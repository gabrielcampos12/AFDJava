    private void jButtonjButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonjButton4ActionPerformed
        for(State state: states){
            if(state.getName().equals(jCBiStateT3.getSelectedItem().toString())){
                for(State state2:states){
                    if(state2.getName().equals(jCBeStateT3.getSelectedItem().toString())){
                        state.getTransition().put(jCBAlphabetT3.getSelectedItem().toString(), state2);
                    }
                }
            }
        }
    }//GEN-LAST:event_jButtonjButton4ActionPerformed

    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        for(State state : states){
            if(state.getName().equals(jListFinals1.getSelectedValue())){
                state.setEnd(true);
                JOptionPane.showMessageDialog(null, "Estado "+state.getName()+" Adicionado no conjunto de estados finais");
            }
        }
    }//GEN-LAST:event_jButton18ActionPerformed

